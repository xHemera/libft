#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((t_byte *)dest)[i] = ((t_byte *)src)[i];
		i++;
	}
	return (dest);
}
